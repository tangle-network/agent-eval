/**
 * RawProviderSink — first-class persistence for the actual HTTP-level
 * request/response bodies of every LLM provider call.
 *
 * Why this is a separate sink from the structured `LlmSpan`:
 *
 *   - `LlmSpan` records the *intent* — model name, messages, output text,
 *     usage. It's what dashboards read; it's NOT enough for forensics.
 *   - When a downstream consumer reports "the verifier used the wrong route"
 *     or "tokens look right but reasoning was missing," the only way to
 *     answer is the raw HTTP body. Span fields can lie (a proxy can echo
 *     a different `model` value than what actually answered); the raw
 *     response is ground truth.
 *
 * Default behaviour: opt-in. Pass `rawSink` to `LlmClientOptions` (or the
 * matrix runner / BuilderSession sets it up automatically) and every
 * request, response, and error is recorded — including retries, with the
 * attempt index attached so a flaky call's full event chain is recoverable.
 *
 * Redaction is enforced at sink time. The default redactor strips
 * `Authorization`, `X-Api-Key`, `X-Auth-Token`, `Cookie` headers and any
 * payload field whose key matches `apiKey | api_key | bearer | password |
 * secret | token` (case-insensitive). Override via the sink constructor or
 * the per-call `redactor`. The `redactedFields` array on the persisted
 * event lets a reviewer see what was stripped without exposing the values.
 */

import { promises as fs } from 'node:fs'
import * as path from 'node:path'

export type RawProviderDirection = 'request' | 'response' | 'error'

export interface RawProviderEvent {
  /** Stable id. Generated by the sink if omitted. */
  eventId: string
  /** Trace context populated by `LlmClient` when the call is wrapped in a span. */
  runId?: string
  spanId?: string
  /**
   * Logical provider name. Free-form so callers can use whatever id matches
   * their topology (`'openai'`, `'anthropic'`, `'tangle-router'`, …). When
   * omitted, derived from `baseUrl` in `LlmClientOptions`.
   */
  provider: string
  model: string
  /** Endpoint path, e.g. `'/v1/chat/completions'`. */
  endpoint: string
  /** Base URL used for the call (already-normalised — no trailing slash). */
  baseUrl: string
  /** 0-indexed retry attempt. The first attempt is 0; a retried call gets 1, 2, … */
  attemptIndex: number
  direction: RawProviderDirection
  /** Unix ms. */
  timestamp: number
  /** Wall-clock duration of the call leg. Set on `response` and `error` events; null on `request`. */
  durationMs?: number
  statusCode?: number
  requestHeaders?: Record<string, string>
  requestBody?: unknown
  responseHeaders?: Record<string, string>
  responseBody?: unknown
  /** Set on `direction: 'error'` events. */
  errorMessage?: string
  /** Field paths the redactor stripped from this event ('header:Authorization', 'body.apiKey', …). */
  redactedFields: string[]
}

export interface RawProviderSinkFilter {
  runId?: string
  spanId?: string
  direction?: RawProviderDirection
  attemptIndex?: number
}

export interface RawProviderSink {
  record(event: RawProviderEvent): Promise<void>
  /** Optional listing — implementations that durably persist (file, db) should support this. */
  list?(filter?: RawProviderSinkFilter): Promise<RawProviderEvent[]>
  /** Optional teardown for backed implementations. */
  close?(): Promise<void>
}

export type ProviderRedactor = (event: RawProviderEvent) => RawProviderEvent

const REDACTED_HEADER_NAMES = new Set([
  'authorization',
  'x-api-key',
  'x-auth-token',
  'cookie',
  'set-cookie',
  'proxy-authorization',
])

const REDACTED_BODY_KEY = /^(api[_-]?key|bearer|password|secret|token|access[_-]?token|refresh[_-]?token)$/i

/**
 * Default redactor — strips well-known auth headers and any body field whose
 * key matches the credential pattern. Records every redacted path on
 * `event.redactedFields` so a downstream reviewer can see what was removed.
 */
export function defaultProviderRedactor(event: RawProviderEvent): RawProviderEvent {
  const redactedFields: string[] = [...(event.redactedFields ?? [])]
  const requestHeaders = redactHeaders(event.requestHeaders, 'request', redactedFields)
  const responseHeaders = redactHeaders(event.responseHeaders, 'response', redactedFields)
  const requestBody = redactBody(event.requestBody, 'requestBody', redactedFields)
  const responseBody = redactBody(event.responseBody, 'responseBody', redactedFields)
  return { ...event, requestHeaders, responseHeaders, requestBody, responseBody, redactedFields }
}

function redactHeaders(
  headers: Record<string, string> | undefined,
  prefix: 'request' | 'response',
  redactedFields: string[],
): Record<string, string> | undefined {
  if (!headers) return headers
  const out: Record<string, string> = {}
  for (const [k, v] of Object.entries(headers)) {
    if (REDACTED_HEADER_NAMES.has(k.toLowerCase())) {
      redactedFields.push(`${prefix}Headers.${k}`)
      continue
    }
    out[k] = v
  }
  return out
}

function redactBody(
  value: unknown,
  pathStr: string,
  redactedFields: string[],
): unknown {
  if (value == null) return value
  if (Array.isArray(value)) return value.map((v, i) => redactBody(v, `${pathStr}[${i}]`, redactedFields))
  if (typeof value === 'object') {
    const out: Record<string, unknown> = {}
    for (const [k, v] of Object.entries(value as Record<string, unknown>)) {
      if (REDACTED_BODY_KEY.test(k)) {
        redactedFields.push(`${pathStr}.${k}`)
        continue
      }
      out[k] = redactBody(v, `${pathStr}.${k}`, redactedFields)
    }
    return out
  }
  return value
}

// ── In-memory ────────────────────────────────────────────────────────────

export interface InMemoryRawProviderSinkOptions {
  redactor?: ProviderRedactor
}

export class InMemoryRawProviderSink implements RawProviderSink {
  private events: RawProviderEvent[] = []
  private redactor: ProviderRedactor

  constructor(opts: InMemoryRawProviderSinkOptions = {}) {
    this.redactor = opts.redactor ?? defaultProviderRedactor
  }

  async record(event: RawProviderEvent): Promise<void> {
    this.events.push(this.redactor({ ...event, redactedFields: event.redactedFields ?? [] }))
  }

  async list(filter: RawProviderSinkFilter = {}): Promise<RawProviderEvent[]> {
    return this.events.filter((e) =>
      (filter.runId === undefined || e.runId === filter.runId) &&
      (filter.spanId === undefined || e.spanId === filter.spanId) &&
      (filter.direction === undefined || e.direction === filter.direction) &&
      (filter.attemptIndex === undefined || e.attemptIndex === filter.attemptIndex),
    )
  }

  size(): number { return this.events.length }
}

export class NoopRawProviderSink implements RawProviderSink {
  async record(): Promise<void> { /* no-op */ }
  /**
   * Returns an empty array. Implemented so `assertRunCaptured` does not
   * trip the `no_raw_sink` issue when a caller explicitly opts out of
   * capture by passing this sink — opt-out is a deliberate choice, not a
   * misconfiguration.
   */
  async list(): Promise<RawProviderEvent[]> { return [] }
}

// ── Filesystem (NDJSON) ──────────────────────────────────────────────────

export interface FileSystemRawProviderSinkOptions {
  /** Directory the NDJSON file is written into. Created if missing. */
  dir: string
  /** File name; default `'raw-provider-events.ndjson'`. */
  fileName?: string
  /** Bytes after which the writer rolls over to a new file (default 32 MiB). */
  rollAtBytes?: number
  redactor?: ProviderRedactor
}

export class FileSystemRawProviderSink implements RawProviderSink {
  private dir: string
  private fileName: string
  private rollAtBytes: number
  private redactor: ProviderRedactor
  private bytesWritten = 0
  private rollIndex = 0
  private initPromise: Promise<void> | null = null

  constructor(opts: FileSystemRawProviderSinkOptions) {
    this.dir = opts.dir
    this.fileName = opts.fileName ?? 'raw-provider-events.ndjson'
    this.rollAtBytes = opts.rollAtBytes ?? 32 * 1024 * 1024
    this.redactor = opts.redactor ?? defaultProviderRedactor
  }

  private async ensureInit(): Promise<void> {
    if (!this.initPromise) {
      this.initPromise = fs.mkdir(this.dir, { recursive: true }).then(() => undefined)
    }
    await this.initPromise
  }

  private currentPath(): string {
    if (this.rollIndex === 0) return path.join(this.dir, this.fileName)
    return path.join(this.dir, `${this.fileName}.${this.rollIndex}`)
  }

  async record(event: RawProviderEvent): Promise<void> {
    await this.ensureInit()
    const redacted = this.redactor({ ...event, redactedFields: event.redactedFields ?? [] })
    const line = JSON.stringify(redacted) + '\n'
    if (this.bytesWritten + line.length > this.rollAtBytes && this.bytesWritten > 0) {
      this.rollIndex += 1
      this.bytesWritten = 0
    }
    await fs.appendFile(this.currentPath(), line, 'utf8')
    this.bytesWritten += line.length
  }

  async list(filter: RawProviderSinkFilter = {}): Promise<RawProviderEvent[]> {
    await this.ensureInit()
    const out: RawProviderEvent[] = []
    for (let i = 0; i <= this.rollIndex; i++) {
      const file = i === 0
        ? path.join(this.dir, this.fileName)
        : path.join(this.dir, `${this.fileName}.${i}`)
      let body: string
      try {
        body = await fs.readFile(file, 'utf8')
      } catch (err) {
        if ((err as NodeJS.ErrnoException).code === 'ENOENT') continue
        throw err
      }
      for (const line of body.split('\n')) {
        if (!line) continue
        const event = JSON.parse(line) as RawProviderEvent
        if (filter.runId !== undefined && event.runId !== filter.runId) continue
        if (filter.spanId !== undefined && event.spanId !== filter.spanId) continue
        if (filter.direction !== undefined && event.direction !== filter.direction) continue
        if (filter.attemptIndex !== undefined && event.attemptIndex !== filter.attemptIndex) continue
        out.push(event)
      }
    }
    return out
  }
}

// ── Helpers ──────────────────────────────────────────────────────────────

/**
 * Best-effort provider id from a base URL. Falls back to the URL host when
 * none of the well-known patterns match.
 */
export function providerFromBaseUrl(baseUrl: string): string {
  const lower = baseUrl.toLowerCase()
  if (lower.includes('api.openai.com')) return 'openai'
  if (lower.includes('api.anthropic.com')) return 'anthropic'
  if (lower.includes('generativelanguage.googleapis.com')) return 'google'
  if (lower.includes('api.together.ai') || lower.includes('api.together.xyz')) return 'together'
  if (lower.includes('api.deepseek.com')) return 'deepseek'
  if (lower.includes('router.tangle.tools')) return 'tangle-router'
  if (lower.includes('api.litellm') || lower.includes('litellm')) return 'litellm'
  try {
    return new URL(baseUrl).host
  } catch {
    return baseUrl
  }
}
